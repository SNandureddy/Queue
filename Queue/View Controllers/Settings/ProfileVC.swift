//MARK: TextField Delegates
extension ProfileVC: UITextFieldDelegate {
    func textFieldShouldReturn(_ textField: UITextField) -> Bool {
        self.view.endEditing(true)
        return true
    }
    
    func textField(_ textField: UITextField, shouldChangeCharactersIn range: NSRange, replacementString string: String) -> Bool {
        guard let text = textField.text else { return true }
        let newLength = text.count + string.count - range.length
        if textField == nameTextField { //Name
            return newLength <= 40
        }
        if textField == emailTextField { //Email
            return newLength <= 80
        }
        return true
    }
}

//MARK: Vaidate Data
extension ProfileVC {
    
    func validateData() -> String? {
        if nameTextField.isEmpty {
            return kEmptyName
        }
        if !nameTextField.isValidName {
            return kValidName
        }
        if emailTextField.isEmpty {
            return kEmptyEmail
        }
        if !emailTextField.isValidEmail {
            return kValidEmail
        }
        return nil
    }
}


//MARK: API Methods
extension ProfileVC {
    
    func getProfile() {
        UserVM.shared.getProfile { (message, error) in
            if error != nil {
                self.showErrorMessage(error: error)
                self.tabBarController?.selectedIndex = 0
            }
            else {
                self.nameTextField.text = UserVM.shared.userDetails.name
                self.emailTextField.text = UserVM.shared.userDetails.email
            }
        }
    }
    
    func updateProfile() {
        if let message = validateData() {
            self.showAlert(title: kError, message: message)
            return
        }
        var dict = JSONDictionary()
        dict[APIKeys.kFullName] = nameTextField.text!
        dict[APIKeys.kEmail] = emailTextField.text!
        UserVM.shared.updateProfile(dict: dict) { (message, error) in
            if error != nil {
                self.showErrorMessage(error: error)
            }
            else {
                DataManager.name = self.nameTextField.text!
                DataManager.email = self.emailTextField.text!
                self.showAlert(title: kSuccess, message: message, cancelTitle: nil, cancelAction: nil, okayTitle: kOkay, {
                    self.navigationController?.popViewController(animated: true)
                })
            }
